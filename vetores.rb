# Um VETOR é Ordenado. É uma coleção indexada POR INTEIROS de qualquer objeto.
# -->               VETOR=[1,2,3,4,5]

# VETOR utiliza colchetes [ ] para determinar um ARRAY

=begin
No IRB:
Determinar o valor do vetor. Ex: Vetor=[]
na sequencia dar o push para converter. EX.:
vetor.push(10,2,3,5,0)
resultado:
[10,2,3,5,0]
O Primeiro elemento é ordenado por 0, depois 1, 2,3,4...

para consulta -->    vetor[0]      ; o resultado será 10
para deletar -->  vetor.delete(10)    ; deletará todos os números 10 que houverem no ARRAY
=end
